//! CONTEXT: Boot splash screen — rendered via VMO writes to the framebuffer after ramfb config.
//! OWNERS: @runtime
//! STATUS: Experimental
//! API_STABILITY: Unstable
//!
//! Writes directly to the framebuffer VMO using `vmo_write`. The splash is pre-computed
//! per-row to minimize VMO write calls (one call per row).

#[cfg(all(feature = "os-lite", nexus_env = "os", target_os = "none"))]
use nexus_abi::vmo_write;
use windowd::VisibleBootstrapMode;

extern crate alloc;

type Pixel = [u8; 4];

const BG_COLOR: Pixel = [10, 22, 40, 255];
const ACCENT_COLOR: Pixel = [58, 134, 255, 255];
const WHITE_COLOR: Pixel = [255, 255, 255, 255];

/// Render the splash to the framebuffer VMO, row by row.
#[cfg(all(feature = "os-lite", nexus_env = "os", target_os = "none"))]
pub fn write_splash(fb_handle: u32, mode: VisibleBootstrapMode) {
    let w = mode.width as usize;
    let h = mode.height as usize;
    let stride = mode.stride as usize; // bytes per row

    // Pre-compute the title text positions
    let title = "OPEN NEXUS OS";
    let scale: usize = 3;
    let char_sz = 8 * scale; // pixels per character
    let title_w = title.len() * char_sz;
    let title_x = (w - title_w) / 2;
    let title_y = h / 2 - char_sz;

    let subtitle = "Microkernel   RISC-V";
    let sub_scale: usize = 1;
    let sub_sz = 8 * sub_scale;
    let sub_w = subtitle.len() * sub_sz;
    let sub_x = (w - sub_w) / 2;
    let sub_y = title_y + char_sz + 16;

    let version = concat!("v", env!("CARGO_PKG_VERSION"), "   QEMU virt");
    let ver_scale: usize = 1;
    let ver_sz = 8 * ver_scale;
    let ver_w = version.len() * ver_sz;
    let ver_x = (w - ver_w) / 2;
    let ver_y = h - 40;

    let box_margin: usize = 60;
    let box_x = title_x.saturating_sub(40);
    let box_y = title_y.saturating_sub(24);
    let box_w = title_w + 80;
    let box_h = char_sz + 60;

    // Allocate one row buffer
    let mut row = alloc::vec![0u8; stride];

    for y in 0..h {
        // Fill with background
        for x in 0..w {
            let off = x * 4;
            row[off..off + 4].copy_from_slice(&BG_COLOR);
        }

        // Accent bars
        if y < 4 || y >= h - 4 {
            for x in 0..w {
                let off = x * 4;
                row[off..off + 4].copy_from_slice(&ACCENT_COLOR);
            }
        }

        // Border box
        let in_box = y >= box_y && y < box_y + box_h;
        if in_box {
            let on_top = y == box_y;
            let on_bot = y == box_y + box_h - 1;
            if on_top || on_bot {
                for x in box_x..box_x + box_w {
                    if x < w {
                        let off = x * 4;
                        row[off..off + 4].copy_from_slice(&ACCENT_COLOR);
                    }
                }
            }
            // left and right edges
            if box_x < w {
                row[box_x * 4..box_x * 4 + 4].copy_from_slice(&ACCENT_COLOR);
            }
            let right_x = box_x + box_w - 1;
            if right_x < w {
                row[right_x * 4..right_x * 4 + 4].copy_from_slice(&ACCENT_COLOR);
            }
        }

        // Title text
        draw_text_row(&mut row, y, title_x, title_y, title, scale, WHITE_COLOR);

        // Subtitle
        draw_text_row(&mut row, y, sub_x, sub_y, subtitle, sub_scale, ACCENT_COLOR);

        // Version
        draw_text_row(&mut row, y, ver_x, ver_y, version, ver_scale, ACCENT_COLOR);

        // Write this row to the VMO
        let _ = vmo_write(fb_handle, y * stride, &row[..stride]);
    }
}

fn draw_text_row(
    row: &mut [u8],
    row_y: usize,
    text_x: usize,
    text_y: usize,
    text: &str,
    scale: usize,
    color: Pixel,
) {
    let char_sz = 8 * scale;
    if row_y < text_y || row_y >= text_y + char_sz {
        return;
    }
    let char_row = (row_y - text_y) / scale;
    for (i, ch) in text.bytes().enumerate() {
        let cx = text_x + i * char_sz;
        let glyph = get_glyph(ch);
        let bits = glyph[char_row.min(7)];
        for col in 0..8 {
            if (bits >> (7 - col)) & 1 != 0 {
                for sx in 0..scale {
                    let x = cx + col * scale + sx;
                    if x * 4 + 4 <= row.len() {
                        row[x * 4..x * 4 + 4].copy_from_slice(&color);
                    }
                }
            }
        }
    }
}

